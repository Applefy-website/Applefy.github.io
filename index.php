                <div class="about-me pt-4 pt-md-0">
                  <div class="title-box-2">
                      Important Rules
                    </h5>
                </div>
                <p class="lead">
                  Good quality microphone is required.
Voice changers must be of good quality.
No high pitched or robotic voices.
Must be 18+ to play.
Do not use overly derogatory language or bigotry.
Always stay in character during a scene. If there's a problem, finish the scene and report it in a ticket.
Create RP for others, not just for yourself.
Failure to roleplay with other players will result in a ban.
All players must follow Twitch TOS (click here to view them) regardless if they are streaming or not because you never know who might have their ‘go pro’ on.
Do not DM staff about in-game issues, please use our ticket system.
Staff will not interfere with RP scenes or ‘decide roleplay’ unless a rule is broken.

                </p>
                <p class="lead">
                  Server/’City’ Specific Rules


Steam/Discord/Character Names
Please make sure that they are appropriate, if not you will be warned, the second offense will be a 24 hour ban, and so forth.
New character names must be realistic. No celebrity or notable names.
Toxicity
Whether in character, out of character, or Discord you should never speak or treat anyone negatively that does not make sense to your character's traits.
Being toxic in any way, shape or form whether it's in the server, discord, can have you removed from the community. If you have an issue, please create a ticket and we can properly discuss it instead of creating unnecessary drama.
Do not use 3rd Party comms
Only use the comms that are provided in rp, for example radios and phones. Using other services such as discord while in the city are prohibited.
Robbing and Killing
You may rob and kill someone for their GUNS, PHONES, AND MEDS only.
There must be a RP reason, ie gang beef, defending territory, etc.
Cops and EMS can not be robbed while on duty.
Using /OOC
OOC should only be used for major issues that prevent roleplay from continuing and should never be used for general/support questions.
Try to refrain from using immersion breaking terms, i.e. “Going in my head”

                </p>
                <p class="lead">
                  Reporting Players
Please be as detailed as possible when reporting other players so staff are able to go over as much information to understand the situation in full. Include their player name, character name, or ID. Provide any video evidence if possible.
Please do not file a report on an incident you are not directly involved in. This is known as “Third Party Reporting” if the ones involved have an issue, they need to submit the report.
4 Player Rule
All criminal activities are limited to 4 players at the start of the initial robbery/heist scene. (May change in future)
All 4 people MUST be at the scene, others cannot join in later, or interfere with the scene
Prohibited Roleplay
Extreme Corrupt DOJ/PD RP, PregnantRP, SuicideRP, TortureRP without the consent of other players or staff is prohibited. SexualAssaultRP and Graphic EroticRP are prohibited in roleplay.
Active Scenes
After leaving an active scene, you can not return.
You can not leave an active scene with LEO, grab more supplies and return to the scene.
Tackling then shooting/cuffing right after to prevent the player from shooting/running is a form of power gaming.
You can’t stay on the scene of a crime longer than necessary, if you have the chance to leave, leave.

                </p>
                <p class="lead">
                  Fail RP/Exploiting
Using a vehicle in an unreasonable way to get away from the cops is not allowed. Example, using a sports car, and driving up a mountain trail.
Using a game mechanic in such a way that it allows you to exploit the game in any way.
Returning to the scene after more than 2 minutes.
Misuse of game mechanics and using means that aren't available to you to gain an advantage.
Not roleplaying your injuries out after being treated by EMS.
Any abuse or exploits in game mechanics are not allowed which includes but not limited to duping and sharing items/money/knowledge between your personas.
Powergaming
Roleplaying having God powers.
Not staying in character if your character has traits that shouldn't go beyond their means.
Metagaming
Using information that your character did not find out in game which includes streamer chats or discord channels to enhance your roleplay.
Actively watching another person's stream while you are currently in the city.
Lurking to give the streamer a view, but the stream should be muted.
Combat Logging
Leaving the server during an active scene is prohibited and will be considered as combat logging which will result in a ban if you do not notify someone via discord, or return to the scene within 5 mins.
If caught, this will be an automatic 24 hour ban.

                </p>
                <p class="lead">
                  Vehicle DeathMatch (VDM)
Killing another player(s) with a vehicle without any intent, story or roleplay behind it.
Automatic 24 hour ban.
Random Death Match (RDM)
Killing another player(s) without any intent, story or roleplay behind it.
Automatic 24 hour ban.
New Life Rule (NLR)
30 min injury cooldown applies to ALL players revived by EMS, Nancy, or respawn.
You may not return to your previous ‘scene’ for 30 mins.
Any events leading up to your character going to ICU must be forgotten. If EMS or Police are on the way or at the scene, you may not respawn.
Any use of /911 or /911a is prohibited if dumped by another player.
Locals will not ‘find’ you or ‘see suspicious activity.’
If you go “unconscious” (/me goes unconscious or any variation) or do something that would make you not remember, then you don’t remember.
This includes getting shot in the head, beat in the head, or any kind of head trauma.

Breaking Character
Do not break character under any circumstances, no matter what happens roll with it. If it is an issue we should know as staff please make a ticket instead of breaking character and attempting to deal with it yourself.
If you are stepping away, please don’t say “I’m going in my head” try to come up with something different, like “I gotta take a phone call.” then do /e phonecall.

Fear RP
You must value your character's life like it is their own. If you have a gun to your head, do not try to run and obey the orders of whoever has the gun to your head.
Think about it logically, it’s fight or flight.
Not fearing for your life when you're threatened.
Warnings/Bans
We have various methods to catch cheaters, if caught, you will be banned, no appeal.
Warnings will be given, and you may be asked to come to staff waiting.
Some violations will get an automatic 24 hour ban.
Bans are 24, 48, 72, Perma-ban.
Bans are given based on offense, and other varying factors
If you are banned, you are welcome to create a ticket to discuss the ban.
Perma bans are appealed on a case by case basis.
The Staff does not like or want to give out bans. Please do not ruin your experience by doing something that would result in a ban.


Injury RP

Don't use EMS just as ‘revive bots’. When being treated by EMS, provide them with as much roleplay as possible which will help both sides to produce a good quality scene. Using /me can go a long way when being treated by EMS. You must rp injuries accurately how they would be handled irl. If you get shot, run over, fall off a building, etc you must RP those injuries. If you just got shot up you would not come out of the hospital right away and be ready to go back out on the town with no injuries whatsoever. You need to RP recovering out those injuries in what seems like a realistic way. RPing your injuries in this way will be rewarding for you in the long run, you will see that it will bring you a lot more RP and will give you more stepping stones along the path of your character’s development.

ICU/Morgue
If the medical staff are not able to save their patient, or the injuries are too severe you may be transported to the ICU or Morgue. You will have to respawn when your timer reaches zero.

                </p>
                <p class="lead">
                  Initiation

There must be verbal initiation to EVERY scenario that includes shooting. This means BOTH sides should say some words before any bullets are shot. This includes shootouts with the police, a different gang, or to anyone.
Examples of verbal initiation are:
“Back up or you will be shot.”
“If you keep following you will be shot.”
“Back up off the block or you’re gonna get shot.”
“Back off or you will regret it.”
“This is for hitting on my lil boo thang, playa.”
“Heard you wanted the shmoke, bud?”
“You been shit talking my mama, mate?”

CopRP
Free passage and no spikes are standard practice, be creative.
Cops can turn down offers in negotiations.
Cops can counter offers in negotiations.
Cops will have their own SOP that they must follow.
Best advice, don’t shoot the cops, like just don’t.
Motorcycles CANNOT be your primary getaway vehicle, but can be swapped to mid chase (subject to change if abused)

Gang RP

Gangs can have an unlimited number of people
8 Player Rule
All criminal activities (Crim v Crim / Gang v Gang) are limited to 8 players at anytime including sharing information over radio or phone as a lookout(s)

</p>
<p class="lead">
  Criminal Activities
  Having a criminal character is well needed in the right context. Going 0-100 in the first few days of the city is not the right way to go. Your character should be learning the city, the people who live in it and it's surroundings before doing any sort of criminal activity.
  If you were recently involved in a scene with police, you should give it some time before getting back into another situation with the police or you will be hit with longer jail times/fines for repeated crimes in a short time span.
  Building up a story or conflict will help your character in the long run with meeting connections and having something to look forward to!
  Gang Wars
  If gangs want to officially “go to war” both heads of the gangs will need to contact staff and inform them of the agreed upon war terms for staff approval.
  Attacking
  Maximum of 8 players
  No swapping players between current scenes
  Defending
  At the time of the first initial shot that was fired, the surrounding players on the defensive side are eligible players to defend that said turf
  Cannot bring in outside forces or other players that were not initially present at the time of the first shot
  This only applies for the defending gang's turf. For all other activities that are gang related that are not on a gangs turf is still capped at max of 4 players
  Other
  If you went down in a gang war, you should not be engaging back into the fight immediately. There is a 30 minute cooldown.
  If you are in a gang war, you should not be shooting from turf to turf/block to block. One side must push the other with only 8 people.
  If engaged outside the block, you can't return until the situation is over.
  For the duration of the Gang War, no initiation is needed for shootouts.
  Inception RP Not To Do’s

</p>
<p class="lead">
  Do NOT do any criminal activity 30 minutes BEFORE a server restart “Tsunami” as it doesnt give enough time for a complete scenario with all parties involved. (Example: Rob, Loot, Steal, Kill, Heists, or any type of illegal activity!!)
  Do NOT do any criminal activity 15 minutes AFTER a server restart.
  Do NOT use your friends as hostages for crime. The hostage must come unwillingly.
  You are NOT allowed to rob cops.
  Cop baiting is a big no.
  There is a big difference between providing roleplay for the cops and cop baiting.
  You may NOT rob, kill, hold EMS hostage, or, rob and steal EMS vehicles.
  Do not drive vehicles up or down stairs, except motorcycles can go downstairs only.
  NO ANIMAL PEDS without EXPRESS permission from staff. No exceptions.
  Do not drive a car in an unrealistic manner.
  Don’t take a lowered sports car off roading or up the side of a mountain.
  Helicopters and Airplanes are strictly forbidden and off limits without express permission from staff. Only exception to this is for LEO and EMS while conducting on duty services.

  Green Zones
  A player may not run into a green zone to stop an ongoing scene.

  Inside Pill box sliding doors
  All PDs
  Apartments
  Clothing, Tattoo, and Barber shops
  Greenzone while actively in a menu
  No gang/group wars or shootouts
  Things to consider
  Locals are civilians just like players, use them as part of your RP if you would like, but remember, your actions towards them is just like an action towards an actual player.

</p>
<p class="lead">
  Motorcycles are great vehicles and can be used to get away from police quickly, but remember, if you use them at the beginning of a scene, you’re only ruining your own RP. Don’t use motorcycles as your primary getaway vehicle.
  Permadeath is a serious event. If you decide to perma-kill a character, it can have impacts on others, and their story lines. Once the character is dead, they may not come back.
  If you come up to a police scene do not try to toss yourself in the middle, but feel free to watch and use it as your own roleplay. Interfering with a police scene can result in FailRP, or violating the 8 Person Rule.
  We are all here to have fun, and play a character, please try not to play ‘yourself.’

  *Rules are subject to change
  **Staff can take action on rule breaks without a player report**

